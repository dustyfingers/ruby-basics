character_name = "George"
character_age = "52"

# when printing a string + variable in ruby, must wrap in parens
puts ("There once was a man named " + character_name + ".")
puts ("He was " + character_age + " years old.")

# you can change the value of variables in ruby
character_name = "Bill"
character_age = "35"

puts ("He really liked the name " + character_name + ".")
puts ("However, he didn't like being " + character_age + ".")